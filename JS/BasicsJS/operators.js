// Basic Arithmetic Operators

let test = 10

// adds 3 to test
console.log(test + 3);

// subtracts 3 from test
console.log(test - 3);

// multiplys 3 times test
console.log(test * 3);

// divides test by 3 
console.log(test / 3);

// test to the power of 3
console.log(test ** 3);

// finds remainder of test divided by 3
console.log(test % 3);

// Incriment test by 1
console.log(test++)

// Decrement test by 1 
console.log(test--)

// adds 20 to test
console.log(test += 20);